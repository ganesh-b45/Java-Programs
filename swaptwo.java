class swaptwo
 {

	public static void main(String[] args) {
		int num1=12;
		int num2=13;
		int temp;
		//swapping two numbers
		System.out.println("num is="+num1);
		System.out.println("num is="+num2);
		temp=num1;
		num1=num2;
		num2=temp;
		System.out.println("num is="+num1);
		System.out.println("num is="+num2);
	}

}